package fr.diginamic.builder;

public class Marque {

	/** nom de la marque*/
	private String nom;

	/** Constructeur de la classe Marque
	 * @param nom
	 */
	public Marque(String nom) {
		this.nom = nom;
	}

	/**
	 * @return the nom
	 */
	public String getNom() {
		return nom;
	}

	/**
	 * @param nom the nom to set
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}
	
	
}
